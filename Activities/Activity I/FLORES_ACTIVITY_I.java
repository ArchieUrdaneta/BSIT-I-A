public class FLORES_ACTIVY_I{
    public static void main (String []args){
        int q = 10;
        int w = 2;
        int x = 15;
        
        int sum= q+w+x;
        int diff= q-w;
        int prod= w*q;
        int quo= q/w;
        System.out.println("Sum=" + sum);
        System.out.println("Difference =" + diff);
        System.out.println("Multiplication= " + prod);
        System.out.println("Quotient= " + quo);
    }
}
